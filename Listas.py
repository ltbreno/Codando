# Lista é uma coleçao ordenada e mutável permitindo ter membros duplicados
# index       0        1        2        3      4
palavras = ["carro","aviao","ferrari","lambo","etc"]
print(palavras)
for palavra in palavras:
    print(palavra)
#A coleção nada mais é que o armazenamento de varias strings em uma so variavel (coleção chamada como "LISTA") 
numero = ["carro", True,2 ,3.5]
